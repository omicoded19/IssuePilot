export function getSkillSuggestions(
  query: string,
  excludedNames: string[] = [],
  limit = 8,
): string[] {
  const normalized = query.trim().toLowerCase()
  if (!normalized) return []

  const excluded = new Set(excludedNames.map((name) => name.trim().toLowerCase()))

  return skillCatalog
    .filter((skill) => !excluded.has(skill.toLowerCase()))
    .filter((skill) => skill.toLowerCase().includes(normalized))
    .sort((left, right) => {
      const leftStarts = left.toLowerCase().startsWith(normalized) ? 0 : 1
      const rightStarts = right.toLowerCase().startsWith(normalized) ? 0 : 1
      return leftStarts - rightStarts || left.localeCompare(right)
    })
    .slice(0, limit)
}
