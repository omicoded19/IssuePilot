export function getRepositoryById(id: string): Repository | undefined {
  return mockRepositories.find((r) => r.id === id)
}

export function getRepositoryAnalysis(id: string): RepositoryAnalysis | undefined {
  if (id === 'appwrite-sdk-for-web') return appwriteSdkAnalysis
  const repo = getRepositoryById(id)
  if (!repo) return undefined
  return { ...appwriteSdkAnalysis, ...repo, overallMatchScore: repo.matchScore }
}

export function parseGithubUrl(url: string): string | null {
  const match = url.match(/github\.com\/([^/]+)\/([^/\s?#]+)/)
  if (!match) return null
  const [, owner, repo] = match
  const slug = `${owner}-${repo}`.toLowerCase()
  const found = mockRepositories.find(
    (r) => r.fullName.toLowerCase() === `${owner}/${repo}`.toLowerCase()
  )
  return found?.id ?? slug
}

export function resolveRepositoryId(input: string): string {
  const fromUrl = parseGithubUrl(input)
  if (fromUrl && getRepositoryById(fromUrl)) return fromUrl
  if (getRepositoryById(input)) return input
  if (fromUrl) return 'appwrite-sdk-for-web'
  return 'appwrite-sdk-for-web'
}
